# 整形
int = 1
print(type(int))

# 浮点数
float = 1.0
print(type(float))

# 复数
complx = 1 + 2j
print(type(complx))

# 布尔值
true = True
false = False
print(type(True))
print(True + False) # True的值为1，False的值为0，所以结果为1

str = "hello world"
# 字符串（str）是Python中用于表示文本的数据类型。
# 字符串由一系列字符组成，可以使用单引号（'）或双引号（"）来定义。
# 字符串是不可变的，这意味着一旦创建了一个字符串，就不能修改它。
print(type(str))
str2 = """1
2
3
4"""
print(str2)